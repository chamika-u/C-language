// Data Types

#include <stdio.h>
int main() {
    int number = 10;
    float decimal = 5.5;
    char character = 'A';
    double largeDecimal = 20.99;

    printf("Integer: %d\n", number);
    printf("Float: %.2f\n", decimal);
    printf("Character: %c\n", character);
    printf("Double: %.2lf\n", largeDecimal);
    return 0;
}